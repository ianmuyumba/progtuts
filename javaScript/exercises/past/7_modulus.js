/*
  Return the Remainder from Two Numbers
    - There is a single operator in JavaScript, capable of providing
    the remainder of a division operation. Two numbers are passed as
    parameters. The first parameter divided by the second parameter
    will have a remainder, possibly zero. Return that value.
*/
var x = 2;
var y = 1;

function remainder(x, y) {
	var myModulus = x % y;
	return myModulus;
}

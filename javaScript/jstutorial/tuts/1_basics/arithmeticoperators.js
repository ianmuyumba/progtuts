/* Arithmetic Operators

	- Addition
	- Subtraction
	- Multiplication
	- Division
	- Modulus

*/

var x = 100;
var y = 20;

var sum1 = x+y; //Sum of the two numbers
console.log(sum1);

var difference1 = x-y; //Difference of the two numbers
console.log(difference1);

var product1 = x*y; //Product of the two numbers
console.log(product1);

var quotient1 = x/y; //Quotient of the two numbers
console.log(quotient1);

var modulus1 = x%y; //Modulus of the two numbers - Returns the remainder
console.log(modulus1);
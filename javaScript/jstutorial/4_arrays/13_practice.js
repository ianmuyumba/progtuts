/*
    - Accept user input and store the values in an array
    - Print out the maximum value in the array
    - Exit
*/

var userInput = prompt("Enter integers of your choice and type exit to close");
var userInput = parseInt(userInput);

var myAges = [];

if (userInput >= 0) {

  do {
    myAges.push();
  } while (userInput >= 0);

} else {
  console.log("You may not have entered an integer. Wacha bangi");
}

console.log(myAges);

/*
    - The "while loop" lets you execute a block of code until a certain condition becomes false
*/

// Count one to ten
counter = 1;

while (counter < 11) { // Set the condition until where the loop will stop executing
  console.log(counter); // Prints out the desired output while the condition is true
  counter++; // Increments the value of the counter variable
}


// Count from five to ten
counter1 = 5;

while (counter1 < 11){
  console.log(counter1);
  counter1++;
}


// Count backwards from ten to one
counter2 = 10;

while (counter2 > 0){
  console.log(counter2);
  counter2--;
}
